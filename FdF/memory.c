#include "fdf.h"

void	free_map(char ***map)
{
	int	i;
	int	j;

	i = -1;
	while (map[++i])
	{
		j = -1;
		while(map[i][++j])
			free(map[i][j]);
		free(map[i]);
	}
	free(map);
}
